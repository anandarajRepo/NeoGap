"""
NeoGap — Domain data models.

All dataclasses are intentionally plain (no ORM, no DB dependencies) so they
can be passed freely between services and serialised to JSON for logging.
"""

from __future__ import annotations

from dataclasses import dataclass, field
from datetime import datetime
from enum import Enum
from typing import Optional


# ---------------------------------------------------------------------------
# Enumerations
# ---------------------------------------------------------------------------

class GapDirection(str, Enum):
    UP = "GAP_UP"
    DOWN = "GAP_DOWN"
    NONE = "NO_GAP"


class SignalDirection(str, Enum):
    BUY = "BUY"       # Trade in gap direction (continuation) or counter (reversal)
    SELL = "SELL"
    NONE = "NONE"


class SignalBasis(str, Enum):
    CONTINUATION = "CONTINUATION"   # Historical gaps tend to continue
    REVERSAL = "REVERSAL"           # Historical gaps tend to fill/reverse
    INSUFFICIENT_DATA = "INSUFFICIENT_DATA"


class OrderSide(str, Enum):
    BUY = "B"
    SELL = "S"


class OrderType(str, Enum):
    MARKET = "MKT"
    LIMIT = "L"
    SL_MARKET = "SL-M"
    SL_LIMIT = "SL"


class PositionStatus(str, Enum):
    OPEN = "OPEN"
    PARTIAL = "PARTIAL"        # Partial exit taken at first target
    CLOSED = "CLOSED"
    STOPPED_OUT = "STOPPED_OUT"


class ExitReason(str, Enum):
    TARGET_1 = "TARGET_1"
    TARGET_2 = "TARGET_2"
    TRAILING_STOP = "TRAILING_STOP"
    STOP_LOSS = "STOP_LOSS"
    END_OF_DAY = "END_OF_DAY"
    MANUAL = "MANUAL"


# ---------------------------------------------------------------------------
# Gap analysis models
# ---------------------------------------------------------------------------

@dataclass
class DayOHLC:
    """One day's OHLC bar for a single symbol."""
    symbol: str
    date: datetime
    open: float
    high: float
    low: float
    close: float
    volume: int


@dataclass
class HistoricalGap:
    """A single historical gap event for trend analysis."""
    date: datetime
    gap_direction: GapDirection
    gap_pct: float          # Positive for gap-up, negative for gap-down
    prev_close: float
    open_price: float
    day_close: float
    # Did price continue in the gap direction (True) or fill/reverse (False)?
    is_continuation: bool
    continuation_pct: float  # How much it continued or reversed (absolute %)


@dataclass
class GapTrend:
    """
    Summary of historical gap behaviour for one symbol.
    Produced by GapTrendService and consumed by GapStrategy.
    """
    symbol: str
    direction: GapDirection         # Direction of gaps analysed (UP or DOWN)
    total_gaps: int
    continuation_count: int
    reversal_count: int

    @property
    def continuation_rate(self) -> float:
        if self.total_gaps == 0:
            return 0.0
        return self.continuation_count / self.total_gaps

    @property
    def reversal_rate(self) -> float:
        if self.total_gaps == 0:
            return 0.0
        return self.reversal_count / self.total_gaps

    avg_gap_pct: float = 0.0
    avg_continuation_pct: float = 0.0
    avg_reversal_pct: float = 0.0
    # Trend score 0–100: higher = stronger directional tendency
    trend_score: float = 0.0


@dataclass
class GapEvent:
    """
    Today's detected gap for a symbol at market open.
    """
    symbol: str
    detected_at: datetime
    gap_direction: GapDirection
    gap_pct: float          # Absolute percentage
    prev_close: float
    open_price: float
    avg_volume_20d: int
    today_volume: int       # Volume in first few minutes

    @property
    def volume_ratio(self) -> float:
        if self.avg_volume_20d == 0:
            return 0.0
        return self.today_volume / self.avg_volume_20d


