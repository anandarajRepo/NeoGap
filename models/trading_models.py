# ---------------------------------------------------------------------------
# Daily metrics
# ---------------------------------------------------------------------------

@dataclass
class StrategyMetrics:
    date: datetime
    total_signals: int = 0
    total_trades: int = 0
    winning_trades: int = 0
    losing_trades: int = 0
    cumulative_pnl: float = 0.0
    max_drawdown: float = 0.0
    daily_loss: float = 0.0
    peak_pnl: float = 0.0

    @property
    def win_rate(self) -> float:
        if self.total_trades == 0:
            return 0.0
        return self.winning_trades / self.total_trades
