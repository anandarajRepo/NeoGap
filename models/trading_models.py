@dataclass
class Position:
    symbol: str
    signal: GapSignal
    entry_price: float
    quantity: int
    order_side: OrderSide
    status: PositionStatus = PositionStatus.OPEN
    entry_order_id: str = ""
    sl_order_id: str = ""
    target_order_id: str = ""
    current_price: float = 0.0
    highest_price: float = 0.0      # for trailing stop on longs
    lowest_price: float = 0.0       # for trailing stop on shorts
    trailing_stop: float = 0.0
    partial_exit_done: bool = False
    realised_pnl: float = 0.0
    unrealised_pnl: float = 0.0
    opened_at: Optional[datetime] = None
    closed_at: Optional[datetime] = None
    exit_reason: Optional[ExitReason] = None

    def update_trailing_stop(self, current_price: float, trailing_pct: float) -> None:
        if self.order_side == OrderSide.BUY:
            if current_price > self.highest_price:
                self.highest_price = current_price
            new_stop = self.highest_price * (1 - trailing_pct)
            if new_stop > self.trailing_stop:
                self.trailing_stop = new_stop
        else:
            if current_price < self.lowest_price or self.lowest_price == 0:
                self.lowest_price = current_price
            new_stop = self.lowest_price * (1 + trailing_pct)
            if self.trailing_stop == 0 or new_stop < self.trailing_stop:
                self.trailing_stop = new_stop

    def compute_unrealised_pnl(self, current_price: float) -> float:
        multiplier = 1 if self.order_side == OrderSide.BUY else -1
        self.current_price = current_price
        self.unrealised_pnl = (current_price - self.entry_price) * self.quantity * multiplier
        return self.unrealised_pnl


